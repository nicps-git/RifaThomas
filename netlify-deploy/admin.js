// Fun√ß√£o principal de atualiza√ß√£o de dados
async function refreshData() {
    if (autoSyncConfig.isUpdating) {
        console.log('‚ö†Ô∏è Atualiza√ß√£o j√° em andamento...');
        return;
    }
    
    console.log('üîÑ Iniciando atualiza√ß√£o manual de dados...');
    autoSyncConfig.isUpdating = true;
    
    try {
        // Mostrar progresso
        showSyncProgress('Conectando...', 10);
        
        // Carregar dados atualizados
        showSyncProgress('Carregando dados...', 30);
        await loadPurchaseData();
        
        // Atualizar interface
        showSyncProgress('Atualizando interface...', 60);
        updateDashboard();
        loadParticipants();
        
        // For√ßar cria√ß√£o de bot√µes se necess√°rio
        showSyncProgress('Verificando bot√µes...', 80);
        setTimeout(() => {
            const buttons = document.querySelectorAll('[data-action]');
            if (buttons.length === 0) {
                console.log('‚ö†Ô∏è Bot√µes n√£o encontrados ap√≥s atualiza√ß√£o, for√ßando cria√ß√£o...');
                window.forcarBotoesEmergencia?.();
            }
        }, 1000);
        
        // Finalizar
        showSyncProgress('Conclu√≠do!', 100);
        autoSyncConfig.lastUpdate = new Date();
        updateLastUpdateDisplay();
        
        console.log('‚úÖ Dados atualizados com sucesso!');
        showNotification('üìä Dados atualizados com sucesso!', 'success');
        
    } catch (error) {
        console.error('‚ùå Erro ao atualizar dados:', error);
        showNotification('‚ùå Erro ao atualizar dados: ' + error.message, 'error');
        showSyncProgress('Erro na atualiza√ß√£o', 0);
    } finally {
        autoSyncConfig.isUpdating = false;
        setTimeout(hideSyncProgress, 2000);
    }
}

// Auto-sync autom√°tico
function startAutoSync() {
    if (autoSyncConfig.timer) {
        clearInterval(autoSyncConfig.timer);
    }
    
    console.log(`üîÑ Auto-sync iniciado (intervalo: ${autoSyncConfig.interval/1000}s)`);
    
    autoSyncConfig.timer = setInterval(async () => {
        if (!autoSyncConfig.isUpdating && document.visibilityState === 'visible') {
            console.log('üîÑ Auto-sync executando...');
            await refreshData();
        }
    }, autoSyncConfig.interval);
    
    updateSyncIndicators();
}

function stopAutoSync() {
    if (autoSyncConfig.timer) {
        clearInterval(autoSyncConfig.timer);
        autoSyncConfig.timer = null;
    }
    console.log('‚è∏Ô∏è Auto-sync pausado');
    updateSyncIndicators();
}

// Toggle do auto-sync
function toggleAutoSync() {
    autoSyncConfig.enabled = !autoSyncConfig.enabled;
    
    if (autoSyncConfig.enabled) {
        startAutoSync();
        showNotification('üîÑ Auto-sync ativado', 'success');
    } else {
        stopAutoSync();
        showNotification('‚è∏Ô∏è Auto-sync pausado', 'warning');
    }
    
    updateSyncIndicators();
}

